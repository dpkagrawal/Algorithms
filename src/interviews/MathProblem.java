package interviews;


public class MathProblem {

	/*
	 * Company: Linked In
	 * Write a program that takes an integer and prints out all ways to multiply smaller 
	 * integers that equal the original number, without repeating sets of factors. In other words, 
	 * if your output contains 4 * 3, you should not print out 3 * 4 again as that would be a repeating set. 
	 * Note that this is not asking for prime factorization only. 
	 * Also, you can assume that the input integers are reasonable in size; correctness is more important than efficiency.

	PrintFactors(12)

	12 * 1
	6 * 2
	4 * 3
	3 * 2 * 2

	PrintFactors(32)

	32 * 1
	16 * 2
	8 * 4
	8 * 2 * 2
	4 * 4 * 2
	4 * 2 * 2 * 2
	2 * 2 * 2 * 2 * 2

	PrintFactors (96)
	96 * 1
	48 * 2
	32 * 3
	24 * 4
	24 * 2 * 2
	16 * 6
	16 * 3 * 2
	12 * 8
	12 * 4 * 2
	12 * 2 * 2 * 2
	8 * 6 * 2
	8 * 4 * 3
	8 * 3 * 2 * 2
	6 * 4 * 4
	6 * 4 * 2 * 2
	6 * 2 * 2 * 2 * 2
	4 * 4 * 3 * 2
	4 * 3 * 2 * 2 * 2
	3 * 2 * 2 * 2 * 2 * 2
	*/
	public static void printFactors(int number) {
        printFactors("", number, number);
    }

    public static void printFactors(String expression, int dividend, int previous) {
        if(expression == "")
            System.out.println(previous + " * 1");

        for (int factor = dividend - 1; factor >= 2; --factor) {
            if (dividend % factor == 0 && factor <= previous) {
                int next = dividend / factor;
                if (next <= factor && next <= previous)
                        System.out.println( expression + factor + " * " + next);

                printFactors(expression + factor + " * ", next, factor);
            }
        }
    }

    public static void main(String[] args) {
        printFactors(12);
    }
}
